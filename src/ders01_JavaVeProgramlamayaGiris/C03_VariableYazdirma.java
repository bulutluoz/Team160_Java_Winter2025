package ders01_JavaVeProgramlamayaGiris;

public class C03_VariableYazdirma {
    public static void main(String[] args) {


        System.out.println("Hello Java"); // Konsolda Hello Java yazdirir


        int a = 70;

        System.out.println(a);  // a "" icinde olmadigindan variable oldugunu anlar
                                // variable'i bulup o anki degerini yazdirir


        a= 80;

        System.out.println(a); // 80


        String isim = "Ramazan";

        System.out.println("isim"); // isim

        System.out.println(isim); // Ramazan

        System.out.println( "Atanan isim : " + isim       ); // Atanan isim : Ramazan







    }
}
